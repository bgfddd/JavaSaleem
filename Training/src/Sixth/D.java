package Sixth;

public abstract class D {
	
	//Abstraction
		/*Abstract methods: method that don't have body know as abstract method
		 * Java don't allows to create abstract methods in non abstract classes
		 * We have to create abstract class to develop abstract methods
		 * Abstract class allows you to create non abstract methods as well*/

	
    //Abstract method
	public abstract void add(); 
	public abstract void sub();
	public abstract void div();
	
	
	//Non Abstract method
	public void addition() {}
	public abstract void subtraction();
	
	public abstract void division();
	
	
	
	
	
	
	
	
	
	
	
	
}
